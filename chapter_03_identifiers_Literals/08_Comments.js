// JavaScript comments

// 1. Single-line comments start with //
// This comment ends at the end of the line.

// 2. Multi-line comments start with /* and end with */
/*
This is a multi-line comment.
It can span multiple lines.
*/

// 3. Comments are ignored by the JavaScript engine
// They do not affect the execution of the program.

// 4. Good commenting rules
// - Use comments to explain why something is done, not obvious code.
// - Keep comments short, clear, and up to date.
// - Avoid too many comments in simple code.
// - Use block comments for longer explanations.

// Example
let name = "Alice"; // This stores the user's name

/*
This example shows:
- single-line comments
- multi-line comments
- comments that explain the code
*/

console.log(name);